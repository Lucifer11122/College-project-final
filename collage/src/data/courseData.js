// Function to get course by ID
export function getCourseById(courseId) {
  return courses.find(course => course.id === courseId);
}

// Function to get all courses
export function getAllCourses() {
  return courses;
}

// Function to get popular courses
export function getPopularCourses(limit = 5) {
  return [...courses]
    .sort((a, b) => b.popularity - a.popularity)
    .slice(0, limit);
}

// Function to get courses by department
export function getCoursesByDepartment(department) {
  return courses.filter(course => course.department === department);
}
